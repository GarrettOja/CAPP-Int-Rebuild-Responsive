      The ASB has developed a process for developing both standards and
      guidance based, in part, on written comments submitted in response to
      exposure drafts and oral testimony.<br><br>
      While USPAP provides a minimum set of quality control standards for the
      conduct of appraisal in the U.S., it does not attempt to prescribe
      specific methods to be used. Rather, USPAP simply requires that
      appraisers be familiar with and correctly utilize those methods which
      would be acceptable to other appraisers familiar with the assignment at
      hand and acceptable to the intended users of the appraisal. USPAP directs
      this through what is called the Scope of Work rule. At the onset of an
      assignment, an appraiser is obligated to gather certain specified
      preliminary data about the project, such as the nature of the property
